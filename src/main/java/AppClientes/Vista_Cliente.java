    /**
     * Esta funcion actualiza los nombres que se guarden en la lista
     */
    private void actuaizarLista() {
        DefaultListModel datos = new DefaultListModel();
        for (int i = 0; i < lista.size(); i++) {
            Cliente client = lista.get(i);
            datos.addElement(client.getCompleteName());
        }
        JL_clientes.setModel(datos);
    }
    


