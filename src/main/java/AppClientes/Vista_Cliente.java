    /**
     * Es la creacion de la lista creada para los clientes 
     * y se asigna de tipo cliente
     *
     */
    private List<Cliente> lista = new ArrayList<Cliente>();

    /**
     * Esta funcion recibe el nombre del capo de texto y lo guarda en la lista
     *
     * @param evt
     */
    private void JB_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_guardarActionPerformed

        
        Cliente client = new Cliente();
        client.setName(JTF_name.getText());
        client.setLastName(JTF_lastName.getText());
        client.setEmail(JTF_email.getText());
        client.setPhone(JTF_phone.getText());
        
        lista.add(client);
        actuaizarLista();
        
        JOptionPane.showMessageDialog(rootPane, "Cliente guardado"+ client.getCompleteName());
        
        limpiarCampos();
    }//GEN-LAST:event_JB_guardarActionPerformed
        private void limpiarCampos(){
        JTF_name.setText("");
        JTF_lastName.setText("");
        JTF_email.setText("");
        JTF_phone.setText("");
    }
        
    /**
     * Funcion que elimina los datos de la tabla
     *
     * @param evt
     */
    private void JB_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_EliminarActionPerformed
        int indice = JL_clientes.getSelectedIndex();
        lista.remove(indice);
        actuaizarLista();
        JOptionPane.showMessageDialog(rootPane, "Se elimino correctamente");
    }//GEN-LAST:event_JB_EliminarActionPerformed

    private void JB_limpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JB_limpiarActionPerformed
        lista.clear();
        actuaizarLista();
        JOptionPane.showMessageDialog(rootPane, "se limpio la tabla");
    }//GEN-LAST:event_JB_limpiarActionPerformed

